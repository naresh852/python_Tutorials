def designerPdfViewer(h, word):
    # Write your code here
    alphabets=['a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t','u','v','w','x','y','z']
    # new_dict = {alphabets:h for alphabets,h in zip(alphabets,h)}
    new_dict={}
    for alphabets,h in zip(alphabets,h):
        new_dict[alphabets]=h
    values=[]
    for i in word:
        values.append(new_dict[i])
    return max(values)*len(values)

